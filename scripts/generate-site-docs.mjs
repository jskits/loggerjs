function generatePackagesPage(packages) {
  const rows = packages
    .map(
      (pkg) =>
        `| [\`${pkg.name}\`](${sourceDirectoryLink(pkg.dir)}) | ${pkg.description} | ${pkg.exports.length} | ${formatList(pkg.dependencies)} |`,
    )
    .join("\n");

  const sections = packages
    .map(
      (pkg) => `## ${pkg.name}

${pkg.description}

- Version: \`${pkg.version}\`
- Source: [${pkg.dir}](${sourceDirectoryLink(pkg.dir)})
- README: [${pkg.dir}/README.md](${sourceFileLink(`${pkg.dir}/README.md`)})
- API report: [${pkg.name}](/reference/api/${pkg.slug})
- Export subpaths: ${formatList(pkg.exports)}
- Runtime dependencies: ${formatList(pkg.dependencies)}
`,
    )
    .join("\n");

  writeFileIfChanged(
    join(referenceRoot, "packages.md"),
    `---
title: ${yamlString("Packages")}
description: ${yamlString("Generated package reference for all LoggerJS workspace packages.")}
---
${generatedNotice}

# Packages

LoggerJS is published as small packages around a dependency-free core. Platform packages re-export \`@loggerjs/core\` and add runtime-specific transports and integrations.

| Package | Description | Export subpaths | Runtime dependencies |
| --- | --- | ---: | --- |
${rows}

${sections}
`,
  );
}

function generateApiPages(reports) {
  rmSync(apiDocsRoot, { recursive: true, force: true });
  mkdirSync(apiDocsRoot, { recursive: true });

  const rows = reports
    .map(
      (report) =>
        `| [\`${report.packageName}\`](/reference/api/${report.slug}) | [api-reports/${report.file}](${sourceFileLink(`api-reports/${report.file}`)}) |`,
    )
    .join("\n");

  writeFileIfChanged(
    join(apiDocsRoot, "index.md"),
    `---
title: ${yamlString("API Reports")}
description: ${yamlString("Generated public API declaration reports for LoggerJS packages.")}
---
${generatedNotice}

# API Reports

These pages mirror the checked-in API reports produced by \`pnpm build && pnpm api:report\`.

| Package | Source report |
| --- | --- |
${rows}
`,
  );

  for (const report of reports) {
    const source = readFileSync(join(apiReportsRoot, report.file), "utf8")
      .replace(`# API Report: ${report.packageName}`, `# ${report.packageName} API`)
      .replace(
        "Update with `pnpm build && pnpm api:report` after intentional public API changes.",
        `Source report: [api-reports/${report.file}](${sourceFileLink(`api-reports/${report.file}`)}).`,
      );

    writeFileIfChanged(
      join(apiDocsRoot, `${report.slug}.md`),
      `---
title: ${yamlString(`${report.packageName} API`)}
description: ${yamlString(`Public declaration report for ${report.packageName}.`)}
---
${generatedNotice}

${source}
`,
    );
  }
}

