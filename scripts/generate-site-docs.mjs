function generateLlmsFiles(packages, reports, exampleItems) {
  mkdirSync(join(docsRoot, "public"), { recursive: true });

  const docsList = docsCatalog
    .map(([title, slug, note]) => `- [${title}](${siteLink(`/${slug}`)}): ${note}`)
    .join("\n");
  const packageList = packages
    .map((pkg) => `- [${pkg.name}](${siteLink("/reference/packages")}): ${pkg.description}`)
    .join("\n");
  const apiList = reports
    .map(
      (report) =>
        `- [${report.packageName} API](${siteLink(`/reference/api/${report.slug}`)}): Public declaration report.`,
    )
    .join("\n");
  const exampleList = exampleItems
    .map(
      (example) =>
        `- [${example.title}](${sourceDirectoryLink(example.dir)}): ${example.description}`,
    )
    .join("\n");

  writeFileIfChanged(
    join(docsRoot, "public", "llms.txt"),
    `# LoggerJS

> A faster, more powerful isomorphic logger: collect, process, deliver through one fast pipeline built around records, middleware, processors, transports, integrations, and transport-owned codecs.

Use this file as the concise entry point for LoggerJS documentation. Prefer the guide and concept docs first, then package/API references when exact exports or subpaths matter. Performance claims are benchmark-environment dependent; use the benchmark docs for methodology and reproduction commands.

## AI Skill

- [AI Skill](${siteLink("/AI-SKILL")}): Install and use the LoggerJS AI skill with coding agents.
- [Skill Source](${sourceDirectoryLink("skills/loggerjs")}): Installable skill package with workflow, references, and project inspector.

## Docs

${docsList}

## Reference

- [Reference Index](${siteLink("/reference/")}): Generated package, API, and example reference.
- [Packages](${siteLink("/reference/packages")}): Package descriptions, export subpaths, dependencies, and source links.
- [API Reports](${siteLink("/reference/api/")}): Public declaration reports generated from built .d.ts files.
- [Examples](${siteLink("/examples")}): Runnable examples and their package dependencies.

## Packages

${packageList}

## Optional

${apiList}

${exampleList}

- [Repository README](${sourceFileLink("README.md")}): GitHub README source.
- [Full LLM context](${siteLink("/llms-full.txt")}): Expanded source-doc context for larger prompts.
`,
  );

  const expandedDocs = [
    ["Repository README", "README.md"],
    ...docsCatalog.map(([title, slug]) => [title, `docs/${slug}.md`]),
    ["AI Skill", "docs/AI-SKILL.md"],
    ...skillContextSources,
  ]
    .map(([title, path]) => {
      const contents = readFileSync(join(repoRoot, path), "utf8").trim();
      return `# ${title}\n\nSource: ${sourceFileLink(path)}\n\n${contents}`;
    })
    .join("\n\n---\n\n");

  writeFileIfChanged(
    join(docsRoot, "public", "llms-full.txt"),
    `# LoggerJS Full LLM Context

> Expanded LoggerJS documentation context generated from repository Markdown sources.

${expandedDocs}
`,
  );
}

